public class LeaderboardEntry {
    private int id;
    private String username;
    private int score;
    private int winStreak;

    public LeaderboardEntry(int id, String username, int score, int winStreak) {
        this.id = id;
        this.username = username;
        this.score = score;
        this.winStreak = winStreak;
    }

    public int getId() {
        return id;
    }

    public String getUsername() {
        return username;
    }

    public int getScore() {
        return score;
    }

    public int getWinStreak() {
        return winStreak;
    }
}
